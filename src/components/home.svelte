<div>
    <p class="my-3">
        7th Heaven is a modification manager for the game Final Fantasy VII for the PC. It is named
        after Tifa's bar and hideout for the group AVALANCHE.
    </p>
    <p class="my-3">
        Mods allow you to change nearly any aspect of the game, including, but not limited to:
        music, movies, SFX, character/NPC/enemy models, gameplay, battle/world/field textures,
        animations, skill/spell effects, user interface, dialogue, tweaks and cheats, and more.
    </p>
    <p class="my-3">
        7th Heaven is unique in that it lets you easily activate and deactivate mods using a
        convenient user interface (GUI) without needing to permanently alter your game or mess with
        complicated mod file/folder installs. You can quickly try out a mod and keep or uninstall it
        without affecting your game installation. It accomplishes this by intercepting (hooking) the
        game's requests for resources (models, music, textures, etc.) and instead supplies an
        alternate copy of that resource from the mods you have installed and activated. There is no
        need to uninstall and reinstall Final Fantasy 7 if a mod causes a problem or is no longer
        desired.
    </p>
